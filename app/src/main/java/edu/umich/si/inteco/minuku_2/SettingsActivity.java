import android.os.Bundle;
import android.preference.PreferenceFragment;

import edu.umich.si.inteco.minuku.config.UserPreferences;
import edu.umich.si.inteco.minuku_2.preferences.TimePreference;

public class SettingsActivity extends BaseActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        getFragmentManager().beginTransaction().replace(android.R.id.content, new MyPreferenceFragment()).commit();
    }

    public static class MyPreferenceFragment extends PreferenceFragment
    {
        @Override
        public void onCreate(final Bundle savedInstanceState)
        {
            super.onCreate(savedInstanceState);
            addPreferencesFromResource(R.xml.activity_settings);
            String startTime = UserPreferences.getInstance().getPreference("startTime");
            String endTime = UserPreferences.getInstance().getPreference("endTime");
            if(startTime!=null)
                findPreference("startTime").setSummary(startTime);
            if(endTime!=null)
                findPreference("endTime").setSummary(endTime);
        }
    }

}